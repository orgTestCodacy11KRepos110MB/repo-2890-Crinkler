_breakpoint:
	db 0xcc
	ret

_AritCodePos:
	;; object
	mov	ecx, [esp + 1*4]
	mov	edx, [ecx + a_intervalsize]
	shr	edx, 3
	mov	eax, 20000000h
	sub	eax, edx
	push	eax
	push	edx
	call	_AritSize
	pop	ecx
	pop	ecx
	mov	ecx, [esp + 1*4]
	mov	edx, [ecx + a_dest_bit]
	shl	edx, 12
	add	eax, edx
	inc	eax
	ret

_AritSize:
	push ebx
	mov	edx, [esp + (1+1)*4]
	mov	ebx, [esp + (1+2)*4]
	add	ebx, edx
	cmp ebx, 4096
	jge	notearlyout
	mov	eax, [_LogTable + ebx*4]
	sub eax, [_LogTable + edx*4]
	pop ebx
	ret
	
notearlyout:
	push esi
	xor	esi, esi

	bsr	ecx, ebx
	bsr	eax, edx
	sub	ecx, 12
	cmovs	ecx, esi
	sub	eax, 12
	cmovs	eax, esi

	shr	ebx, cl
	mov	esi, ecx
	mov	ecx, eax
	mov	eax, [_LogTable + ebx*4]
	shr	edx, cl
	sub	esi, ecx
	shl	esi, 12
	sub	eax, [_LogTable + edx*4]
	add	eax, esi

	pop	esi
	pop	ebx
	ret
	
	;; object, dest ptr
_AritCodeInit:
	mov	eax, [esp + 1*4]
	mov	edx, [esp + 2*4]
	mov	dword [eax + a_dest_ptr], edx
	mov	dword [eax + a_dest_bit], -1
	mov	dword [eax + a_intervalsize], 80000000h
	mov	dword [eax + a_intervalmin], 0
	ret

	;; object, zero prob, one prob, bit
_AritCode:
	push ebx
	push edi
	push ebp
	push esi
	mov	esi, [esp + (4+1)*4]
	mov	edx, [esp + (4+2)*4]
	mov	ebx, [esp + (4+3)*4]

	mov	edi, [esi + a_dest_ptr]
	mov	ebp, [esi + a_dest_bit]
	mov	eax, [esi + a_intervalsize]
	mov	ecx, [esi + a_intervalmin]

	;; ebp = dest bit index
	;; ecx = interval min
	;; eax = interval size
	;; edx = zero prob
	;; ebx = one prob
	;; edi = dest ptr

	add	ebx, edx
	mul	edx
	div	ebx
	;; eax = threshold value between 0 and 1

	test [esp + (4+4)*4], byte 1
	jz	.zero
	add	ecx, eax
	jnc	.not_overflow
	call	PutBit
.not_overflow:
	sub	eax, [esi + a_intervalsize]
	neg eax
.zero:
	;; ecx = new interval min
	;; eax = new interval size

	test	eax, eax
	js	.filled
.floop:
	inc	ebp
	add	ecx, ecx
	jnc	.not_one
	call	PutBit
.not_one:
	add	eax, eax
	jns	.floop
.filled:

	mov	[esi + a_dest_bit], ebp
	mov	[esi + a_intervalsize], eax
	mov	[esi + a_intervalmin], ecx

	pop esi
	pop ebp
	pop edi
	pop ebx
	ret

PutBit:
	mov	ebx, ebp
.carry_loop:
	dec	ebx
	js	.not
	btc	[edi], ebx
	jc	.carry_loop
.not:
	ret

	;; object
_AritCodeEnd:
	pusha
	mov	ebx, [esp + (8+1)*4]
	mov	edi, [ebx + a_dest_ptr]
	mov	ebp, [ebx + a_dest_bit]
	mov	eax, [ebx + a_intervalsize]
	mov	ecx, [ebx + a_intervalmin]

	add	ecx, eax
	jc	.over
	inc	ebp
.over:
	call	PutBit
;	add	ebp, byte 7
;	shr	ebp, 3
	mov	[esp + 7*4], ebp
	popa
	ret

	;; object, dest ptr
_AritDecodeInit:
	push	ebx
	mov	edx, [esp + 3*4]
	xor	ecx, ecx
.loop:
	bt	[edx], ecx
	adc	ebx, ebx
	inc	ecx
	cmp	ecx, 32
	jl	.loop

	mov	eax, [esp + 2*4]
	mov	dword [eax + a_dest_ptr], edx
	mov	dword [eax + a_dest_bit], ecx
	mov	dword [eax + a_intervalsize], -1
	mov	dword [eax + a_intervalmin], ebx
	pop	ebx
	ret

	;; object, zero prob, one prob
_AritDecode:
	pusha
	mov	ebx, [esp + (8+1)*4]
	mov	edi, [ebx + a_dest_ptr]
	mov	ebp, [ebx + a_dest_bit]
	mov	eax, [ebx + a_intervalsize]
	mov	ecx, [ebx + a_intervalmin]

	mov	edx, [esp + (8+2)*4]
	mov	ebx, [esp + (8+3)*4]

	;; ebp = source bit index
	;; ecx = data
	;; eax = interval size
	;; edx = zero prob
	;; ebx = one prob

	add	ebx, edx
	push	eax
	mul	edx
	div	ebx
	;; eax = threshold value between 0 and 1

	xor	ebx, ebx
	pop	edx
	cmp	ecx, eax
	jb	.zero
	sub	ecx, eax
	xchg	eax, edx
	sub	eax, edx
	inc	ebx
.zero:
	;; ecx = new data
	;; eax = new interval size

	test	eax, eax
	js	.filled
.floop:
	bt	[edi], ebp
	adc	ecx, ecx
	inc	ebp
	add	eax, eax
	jns	.floop
.filled:
	;; ebx = bit

	mov	[esp + 7*4], ebx

	mov	ebx, [esp + (8+1)*4]
	mov	[ebx + a_dest_bit], ebp
	mov	[ebx + a_intervalsize], eax
	mov	[ebx + a_intervalmin], ecx

	popa
	ret

